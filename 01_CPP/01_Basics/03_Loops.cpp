#include <iostream>
using namespace std;

int main() {
    //print numbers from 1 to 5 using FOR Loop
    for (int i = 1; i <= 5; i++) {
        cout << i << " ";
    }
    cout << endl;

    //--------------------------------------------------------

    //print numbers from 1 to 5 using WHILE Loop
    int j = 1;
    while (j <= 5) {
        cout << j << " ";
        j++;
    }
    cout << endl;

    return 0;
}